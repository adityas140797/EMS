<h2 style="text-align: center;">Enquiry Management&nbsp;</h2>
<center> EMS enables the effective and efficient electronic management of enquiries and requests. Gain quick and easy access to all enquiries and relevant information, including a record of all actions and interactions against an enquiry record along with any attached documents. Any enquiries received can be allocated to teams or individuals. <br/>
<br />
Enquiries can be received electronically direct from online forms, or manually entered. <br />
<br />
<br />
<div style="text-align: center;"><img alt="" src="https://www.evolutive.co.uk/images/cms/online%20forms%20(for%20web).png" /></div>
<h2 style="text-align: center;">Online Forms </h2>
<p>EMS can be fully integrated with your online enquiry forms. These can vary from a simple request for 'further information' or eligibility check, through to a complex RGF (Regional Growth Fund) application form including the attachment of electronic documents to support the application. (See Business Support for further information on grant and funding management).<br />
<br />
The flexibility of EMS allows the project profile to be configured to receive all the information entered via the form to be stored in the system for review and where necessary, analysis. </p>
<br />
<br />
<div style="text-align: center;"><img alt="" src="https://www.evolutive.co.uk/images/cms/reporting%20&amp;%20analysis%20(for%20web).png" /></div>
<h2 style="text-align: center;">Reporting &amp; Analysis</h2>
<p>EMS facilitates analysis of all activity across the system, including enquiries, through a collection of standard reports. Standard, bespoke reports to exact personal requirements can be created instantly with EMS's inbuilt report writer. The writer allows for template creation, giving 'drag and drop' access to all fields in the database. <br />
<br />
Reports can be exported into the most common formats including Word, Excel, PDF and HTML. </p> </center>
